Este é um programa de gerenciamente de funcionários em Java utilizando XHTML
e banco de dados SQL para realização de CRUD. É realizado o cadastro, pesquisa,
atualização de dados, exclusão e armazenamento no BD.

This is an employees management system made in Java with XHTML and SQL Database, using the CRUD logic.
What it does is cadastrate, search, update, delete and store in DB.
